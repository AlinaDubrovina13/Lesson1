//(Задание со звездочкой - необязательное) Нарисуй с помощью for треугольник
    public class homework5_3 {
        public static void main(String[] args) {
            int height = 5;

            for (int i = 1; i <= height; i++) {
                for (int j = 1; j <= i; j++) {
                    System.out.print("*");
                }
                System.out.println();
            }
        }
    }
 //Внешний цикл (i от 1 до height) отвечает за количество строк — сколько всего рядов звёздочек будет напечатано.
//Внутренний цикл (j от 1 до i) отвечает за количество звёздочек в текущей строке.
// Обрати внимание: условие j <= i, а не j <= height — именно поэтому в первой строке 1 звёздочка, во второй — 2,
// и так далее, пока не дойдём до height звёздочек в последней строке.
//System.out.print("*") печатает звёздочку без перехода на новую строку
// (это важно — иначе все звёздочки окажутся в отдельных строках).
