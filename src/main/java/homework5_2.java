//Циклический оператор for.
//Выведи таблицу умножения на 5 с помощью цикла for
    public class homework5_2 {
        public static void main(String[] args) {
            int number = 5;

            for (int i = 1; i <= 10; i++) {
                System.out.println(number + " x " + i + " = " + (number * i));
        }
    }
}
//for (int i = 1; i <= 10; i++) — цикл выполняется 10 раз, начиная с i = 1 и заканчивая i = 10.
//На каждой итерации выводится строка вида 5 x i = результат, где результат вычисляется как number * i.
//Структура цикла for удобна здесь, потому что заранее известно точное количество повторений (10 раз)
// — это как раз тот случай, когда for предпочтительнее while.